package Modelo;

/**
 *
 * @author PC
 */
public class Cinta {
    private String idCinta;
    private String nomPelicula;
    private String estadoPelicula;
    private String Actor;

    public String getIdCinta() {
        return idCinta;
    }

    public void setIdCinta(String idCinta) {
        this.idCinta = idCinta;
    }

    public String getNomPelicula() {
        return nomPelicula;
    }

    public void setNomPelicula(String nomPelicula) {
        this.nomPelicula = nomPelicula;
    }

    public String getEstadoPelicula() {
        return estadoPelicula;
    }

    public void setEstadoPelicula(String estadoPelicula) {
        this.estadoPelicula = estadoPelicula;
    }

    public String getActor() {
        return Actor;
    }

    public void setActor(String Actor) {
        this.Actor = Actor;
    }
    
    
}
